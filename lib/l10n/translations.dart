  /// Get a translation by key and language
  static String get(String key, String lang) {
    final translation = _translations[key];
    if (translation == null) return key;
    return translation[lang] ?? translation['en'] ?? key;
  }

  /// Get weekday name by day number (0 = Monday, 6 = Sunday)
  static String getWeekday(int day, String lang) {
    const weekdays = [
      'monday',
      'tuesday',
      'wednesday',
      'thursday',
      'friday',
      'saturday',
      'sunday'
    ];
    final dayKey = weekdays[day % 7];
    return get(dayKey, lang);
  }
